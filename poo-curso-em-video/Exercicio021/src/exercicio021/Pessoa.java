public class Pessoa {
    private String nome;
    private char sexo;
    private String identidade;
    private int dia;
    private int mes;
    private int ano;

    public Pessoa(String nome, char sexo, String identidade, int dia, int mes, int ano) {
        this.nome = nome;
        this.sexo = sexo;
        this.identidade = identidade;
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }
    
    
}
